package pokemon.server.dto;

public class TeamInfo {
    private Integer id;
    private String name;

    public TeamInfo() {}

    public TeamInfo(Integer id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }
}